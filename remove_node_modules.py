import os
import shutil


def remove_node_modules(path):
    # Traverse all directories and remove node_modules folder
    for root, dirs, files in os.walk(path):
        if 'node_modules' in dirs:
            node_modules_path = os.path.join(root, 'node_modules')
            print(f"Removing {node_modules_path}")
            try:
                # Remove the node_modules folder
                shutil.rmtree(node_modules_path)
            except Exception as e:
                print(f"Error occurred while removing {node_modules_path}: {e}")

# Specify the root directory to start traversing from
root_directory = '.'

# Call the function to remove node_modules folders
remove_node_modules(root_directory)
